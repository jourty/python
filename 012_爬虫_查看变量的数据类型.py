#type方法判断变量的数据类型
# 格式： type(变量)

#int
a=1;
print(a)
# <class 'int'>
print(type(a))

#float
b=1.2
print(b)
print(type(b))

#boolrean
c=True
print(c)
print(type(c))

#String
str='xie'
print(str)
print(type(str))

# list
name_list=['1','2']
print(name_list)
print(type(name_list))

#tuple
name_tuple=(12,34,56)
print(name_tuple)
print(type(name_tuple))

# dict
name={'name':'zx'}
print(name)
print(type(name))
